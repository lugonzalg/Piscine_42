void	ft_ultimate_div_mod(int *a, int *b)
{
	int	i_var1;
	int	i_var2;

	i_var1 = *a;
	i_var2 = *b;
	*a = i_var1 / i_var2;
	*b = i_var1 % i_var2;
}
