#include <unistd.h>

void	ft_putchar(char n)
{
	write(1, &n, 1);
}

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (*str != '\0')
	{
		str++;
		i++;
	}
	return (i);
}
